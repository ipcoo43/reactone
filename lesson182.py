# 병합 정렬
# 리스트 안의 자료를 작은 수부터 큰 수 순서로 배열

# 병합 정렬로 줄 세우기 
# 학생 스스로 알아서 줄을 설 수 있는 방법
# 열 명이나 되는 학생들에게 동시에 알아서 줄을 서라고 하면 소란스러울 것이다
# 다섯 명씩 두 조로 나누어 그 안에서 키 순서로 줄을 서라고 한다
# 키 순서대로 정렬된 두 줄이 있다.
# 각 줄 앞에 맨 앞에 있는 두 학생 중에 키가 더 작은 민수를 뽑아 
# 최종 결과 줄에 세운다. 그리고 다시 각 중간 중의 맨 앞에 있는 두 학생을
# 비교해 더 작은 학생을 최종 결과 민수 뒤에 세운다
# 이 과정을 반복하다가 중간 결과 줄 하나가 사라지면 중간 결과 줄에 있는
# 사람 전부 최종 결과 줄에 세운다.