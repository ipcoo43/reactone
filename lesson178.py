# 삽입 정렬로 줄 세우기
# 학생 열명 모인 운동장에 선생님이 등장 한다.
# 열명 중 제일 앞에 있던 승규에게 나와서 줄을 서라고 한다.
# 승규가 나갔으니 이제 아홉명 남았다
# 준호에게 키를 맞춰 줄을 서라고 한다.
# 준호는 이미 줄을 선 승규보다 자신이 키가 작은 것을 확인하고 승규 앞에 선다
# 남은 여덟 명 중 이번에는 민성이가 뽑혀 줄을 선다
# 민성이는 준호보다는 크고 승규보다는 작다
# 준호와 승규 사이 공간에 줄을 선다(삽입)
# 남은 학생을 한 명씩 뽑아 이미 줄을 선 학생 사이사이에 키을 맞춰 끼워 넣는다.
# 마지막 학생까지 줄을 세우면 모든 학생이 제자리에 줄을 서게 된다.